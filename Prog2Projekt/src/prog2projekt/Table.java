    private void enterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterBtnActionPerformed
        
        placeholder = isbnTF.getText();
        
        //error handling
        try{
            isbn = Integer.parseInt(placeholder.trim());
        }catch(NumberFormatException e){
            System.out.println("not a number");
        }
        //error ends
        titel = titelTF.getText();
        
        placeholder = preisTF.getText();
        //error handling
        try{
            preis = Float.parseFloat(placeholder.trim());
            preis = Float.valueOf(df.format(preis));
        }catch(NumberFormatException e){
            System.out.println("not a number");
        }
        //error ends    
        
        if(checkbox.isSelected()){
            auflager = true;
        }else{
            auflager = false;
        }
        
        if(auflager){
            model.insertRow(model.getRowCount(), new Object[]{isbn, titel, preis, "Ja"});
        }else{
            model.insertRow(model.getRowCount(), new Object[]{isbn, titel, preis, "Nein"});
        }
        //model.insertRow(model.getRowCount(), new Object[]{idTF.getText(), nameTF.getText(), ageTF.getText(), checkbox.getText()});
        
        //reset
        titel = "";
        isbn = 0;
        preis = 0.00f;
        
    }//GEN-LAST:event_enterBtnActionPerformed

